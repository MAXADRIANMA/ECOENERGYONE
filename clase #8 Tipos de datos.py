#TIPOS DE DATOS
print("Tipos de datos")

#TIpo de dato entero
numero = 15
print("ESTE ES UN NUMERO DE TIPO ENTERO")
print(numero,type(numero))

#Tipo de dato flotante
numero_flotante = 15.5
print("Este es un numero flotante")
print(numero_flotante,type(numero_flotante))

#Tipo de dato numero complejo
numero_complejo = 5 + 6j
print("Este es un numero complejo")
print(numero_complejo,type(numero_complejo))

#Tipo de dato string
nombre = "Ernesto"
print("este es un string")
print(nombre, type(nombre))

#tipo de dato booleano
verdadero_falso = 3 == 3
print("Este un dato booleano")
print(verdadero_falso, type(verdadero_falso))









